"""
_email_report.py  —  Sends the clinical PDF report to the PATIENT's email only.
The sender (msai51553@gmail.com) is only used as the sending account, not a recipient.
"""

import smtplib
import ssl
from email.mime.multipart import MIMEMultipart
from email.mime.base import MIMEBase
from email.mime.text import MIMEText
from email import encoders
from datetime import datetime


def send_report_email(
    sender_email: str,
    sender_password: str,
    recipient_email: str,       # patient's email — ONLY this person gets the report
    pdf_bytes: bytes,
    filename: str,
    report_type: str = "Clinical",
    patient_summary: str = "",
) -> tuple[bool, str]:
    """
    Send the PDF report to the patient's email ONLY.
    sender_email is used to authenticate with Gmail but does NOT receive the report.
    """

    now_str = datetime.now().strftime("%d %B %Y at %I:%M %p")
    subject = f"Alzheimer's {report_type} Assessment Report — {now_str}"

    body_html = f"""
    <html>
    <body style="font-family:Arial,sans-serif;background:#f4f4f8;padding:0;margin:0;">
      <div style="max-width:600px;margin:30px auto;background:#ffffff;
                  border-radius:12px;overflow:hidden;
                  box-shadow:0 4px 20px rgba(0,0,0,0.08);">

        <!-- Header -->
        <div style="background:linear-gradient(135deg,#4c1d95,#7c3aed);
                    padding:28px 32px;text-align:center;">
          <h1 style="color:#fff;margin:0;font-size:22px;">
            Alzheimer's Prediction System
          </h1>
          <p style="color:#e9d5ff;margin:6px 0 0;font-size:13px;">
            AI-Powered Clinical Decision Support
          </p>
        </div>

        <!-- Body -->
        <div style="padding:28px 32px;">
          <p style="color:#374151;font-size:15px;line-height:1.7;">
            Dear Patient / Clinical Team,
          </p>
          <p style="color:#374151;font-size:15px;line-height:1.7;">
            Please find attached your <strong>{report_type} Assessment Report</strong>
            generated on <strong>{now_str}</strong>.
          </p>

          { "<div style='background:#f5f3ff;border-left:4px solid #7c3aed;padding:14px 18px;border-radius:6px;margin:18px 0;'><p style='margin:0;color:#4c1d95;font-size:14px;font-weight:bold;'>Report Summary</p><p style='margin:6px 0 0;color:#374151;font-size:14px;line-height:1.6;'>" + patient_summary + "</p></div>" if patient_summary else "" }

          <div style="background:#fef3c7;border:1px solid #fbbf24;border-radius:8px;
                      padding:14px 18px;margin:18px 0;">
            <p style="margin:0;color:#92400e;font-size:13px;line-height:1.6;">
              Disclaimer: This report is generated by an AI system for research
              and educational purposes only. It does not constitute a clinical
              medical diagnosis. Please consult a qualified neurologist.
            </p>
          </div>

          <p style="color:#6b7280;font-size:13px;">
            The PDF report is attached. Open it with any PDF reader.
          </p>
        </div>

        <!-- Footer -->
        <div style="background:#f9fafb;border-top:1px solid #e5e7eb;
                    padding:16px 32px;text-align:center;">
          <p style="color:#9ca3af;font-size:12px;margin:0;">
            Alzheimer's Prediction System &nbsp;|&nbsp; Final Year Project &nbsp;|&nbsp; {now_str}
          </p>
        </div>
      </div>
    </body>
    </html>
    """

    try:
        msg = MIMEMultipart("mixed")
        msg["From"]    = sender_email.strip()
        msg["To"]      = recipient_email.strip()    # ← ONLY the patient receives this
        msg["Subject"] = subject

        # HTML body
        msg.attach(MIMEText(body_html, "html"))

        # PDF attachment
        part = MIMEBase("application", "pdf")
        part.set_payload(pdf_bytes)
        encoders.encode_base64(part)
        part.add_header("Content-Disposition", f'attachment; filename="{filename}"')
        msg.attach(part)

        # Send via Gmail SMTP
        context = ssl.create_default_context()
        with smtplib.SMTP("smtp.gmail.com", 587) as server:
            server.ehlo()
            server.starttls(context=context)
            server.login(sender_email.strip(), sender_password)
            server.sendmail(
                sender_email.strip(),
                [recipient_email.strip()],   # ← only patient's email
                msg.as_string()
            )

        return True, ""

    except smtplib.SMTPAuthenticationError:
        return False, (
            "Gmail authentication failed.\n\n"
            "Make sure:\n"
            "1. You are using an App Password (16 chars), NOT your real Gmail password\n"
            "2. 2-Step Verification is enabled on your Google account\n"
            "3. The App Password has no spaces\n\n"
            "Create one at: Google Account → Security → 2-Step Verification → App Passwords"
        )
    except smtplib.SMTPRecipientsRefused:
        return False, "Invalid recipient email address. Please check the patient's email."
    except smtplib.SMTPException as e:
        return False, f"SMTP error: {e}"
    except Exception as e:
        return False, f"Error: {e}"
